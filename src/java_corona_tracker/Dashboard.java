    public void load_states()
    {
        try{
            
            state_api();
            
            JSONObject myresponse = new JSONObject(response.toString());
            
            JSONArray ja = myresponse.getJSONArray("statewise");
            
            JSONObject state_name = (JSONObject) ja.get(0);
            
            if(state_name.getString("deltaconfirmed").equals("0"))
            {

            jLabel12.setText("No Updates yet.....");
            jLabel12.revalidate();
            jLabel12.repaint();                
            
            
            
            
            }
            else{
                                
            jLabel12.setText(""+state_name.getString("deltaconfirmed"));
            jLabel12.revalidate();
            jLabel12.repaint();

            }
            
            if(state_name.getString("deltarecovered").equals("0"))
            {
            
            jLabel15.setText("No Updates yet.....");
            jLabel15.revalidate();
            jLabel15.repaint();
            }
            else{
            
            jLabel15.setText(""+state_name.getString("deltarecovered"));
            jLabel15.revalidate();
            jLabel15.repaint();
            }
            
            if(state_name.getString("deltadeaths").equals("0"))
            {

                
            jLabel17.setText("No Updates yet.....");
            jLabel17.revalidate();
            jLabel17.repaint();
                
            }
            else{

            jLabel17.setText(""+state_name.getString("deltadeaths"));
            jLabel17.revalidate();
            jLabel17.repaint();

            }
            
            int conf = Integer.parseInt(state_name.getString("deltaconfirmed"));
            int rec  = Integer.parseInt(state_name.getString("deltarecovered"));
            
            int act = conf - rec;
            
            if(act == 0)
            {
           
            jLabel19.setText("No Updates yet.....");
            jLabel19.revalidate();
            jLabel19.repaint();   
                
                
            }
            else{
            String active_case = String.valueOf(act);
            
            jLabel19.setText(""+active_case);
            jLabel19.revalidate();
            jLabel19.repaint();
            
            
            
        }
        }
        catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,"An Error :"+e);
                e.printStackTrace();
            }
    }
    
    
